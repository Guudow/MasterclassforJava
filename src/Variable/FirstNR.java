package Variable;

public class FirstNR {
    public static void main(String[] args) {
        int MyFirstNumber = 5;
        int MySecondNumber = 10 + 5;
        int MyThirdNumber = 10 * 5;
        int MyfourNumber = 100 / 10;
        System.out.println(MyFirstNumber);
        System.out.println(MySecondNumber);
        System.out.println(MyThirdNumber);
        System.out.println(MyfourNumber);

        int MyTotal = MyFirstNumber + MySecondNumber * 2;
        System.out.println(MyTotal);

        int MySecondTotal = MyThirdNumber * MyfourNumber / 3;
        System.out.println(MySecondTotal);
    }
}
